// Convert camelCase/PascalCase to kebab-case
function toKebabCase(str) {
  return str
    .replace(/([a-z])([A-Z])/g, "$1-$2")
    .replace(/([A-Z])([A-Z][a-z])/g, "$1-$2")
    .toLowerCase();
}

function processFile(filePath) {
  let content = fs.readFileSync(filePath, "utf8");
  let modified = false;

  // Match import statements with lucide-react
  const importRegex = /import\s*\{([^}]+)\}\s*from\s*["']lucide-react["'];?/g;
  const matches = [...content.matchAll(importRegex)];

  if (matches.length === 0) return false;

  matches.forEach((match) => {
    const fullImport = match[0];
    const iconsStr = match[1];

    // Parse individual icons
    const icons = iconsStr
      .split(",")
      .map((icon) => icon.trim())
      .filter((icon) => icon);

    // Create individual imports
    const newImports = icons
      .map((icon) => {
        const kebabName = iconNameMap[icon] || toKebabCase(icon);
        return `import ${icon} from 'lucide-react/dist/esm/icons/${kebabName}';`;
      })
      .join("\n");

    // Replace the import
    content = content.replace(fullImport, newImports);
    modified = true;
  });

  if (modified) {
    fs.writeFileSync(filePath, content);
    console.log(`✅ Fixed imports in: ${path.relative(process.cwd(), filePath)}`);
    return true;
  }

  return false;
}

// Find all TypeScript/JavaScript files
const files = glob.sync("app/**/*.{ts,tsx,js,jsx}", {
  ignore: ["**/node_modules/**", "**/dist/**", "**/build/**"],
});

console.log(`Found ${files.length} files to process...`);

let processedCount = 0;
files.forEach((file) => {
  if (processFile(file)) {
    processedCount++;
  }
});

console.log(`\n✅ Processed ${processedCount} files with lucide-react imports`);
